          );

          if (isDuplicate) {
            console.log(`è·³è¿‡é‡å¤æ–‡ä»¶: ${fileEntry.name}`);
            continue;
          }

          const file = await fileEntry.handle.getFile();
          const exifData = await extractExif(file);

